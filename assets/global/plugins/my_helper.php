	if (!function_exists('myunlink'))
	{
		function myunlink()
		{
			$list = array();
			for ($i = 0; $i < func_num_args() ; $i++) { 
				if( file_exists(func_get_arg($i)) AND func_get_arg($i) != "" ){
					unlink(func_get_arg($i));
					$list[func_get_arg($i)]=true;
				}else{
					$list[func_get_arg($i)]=false;
				}
			}
			return $list;
		}
	}


